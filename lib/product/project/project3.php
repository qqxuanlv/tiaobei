						Comp = 0;
					}
					GetObj('ISL_Cont').scrollLeft -= num;
					setTimeout('CompScr()', Speed);
				} else { //下翻
					if(Comp > Space) {
						Comp -= Space;
						num = Space;
					} else {
						num = Comp;
						Comp = 0;
					}
					GetObj('ISL_Cont').scrollLeft += num;
